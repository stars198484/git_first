public class Coordinate {
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public Coordinate() {
        this.x = 0;
        this.y = 0;
    }
}
